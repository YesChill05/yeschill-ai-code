    /**
     * 下载应用代码
     *
     * @param appId    应用ID
     * @param request  请求
     * @param response 响应
     */
    @GetMapping("/download/{appId}")
    public void downloadAppCode(@PathVariable Long appId,
                                HttpServletRequest request,
                                HttpServletResponse response) {
        // 1. 基础校验
        ThrowUtils.throwIf(appId == null || appId <= 0, ErrorCode.PARAMS_ERROR, "应用ID无效");
        // 2. 查询应用信息
        App app = appService.getById(appId);
        ThrowUtils.throwIf(app == null, ErrorCode.NOT_FOUND_ERROR, "应用不存在");
        // 3. 权限校验：只有应用创建者可以下载代码
        User loginUser = userService.getLoginUser(request);
        if (!app.getUserId().equals(loginUser.getId())) {
            throw new BusinessException(ErrorCode.NO_AUTH_ERROR, "无权限下载该应用代码");
        }
        // 4. 构建应用代码目录路径（生成目录，非部署目录）
        String codeGenType = app.getCodeGenType();
        String sourceDirName = codeGenType + "_" + appId;
        String sourceDirPath = AppConstant.CODE_OUTPUT_ROOT_DIR + File.separator + sourceDirName;
        // 5. 检查代码目录是否存在
        File sourceDir = new File(sourceDirPath);
        ThrowUtils.throwIf(!sourceDir.exists() || !sourceDir.isDirectory(),
                ErrorCode.NOT_FOUND_ERROR, "应用代码不存在，请先生成代码");
        // 6. 生成下载文件名
        String downloadFileName = String.valueOf(appId);
        // 7. 调用通用下载服务
        projectDownloadService.downloadProjectAsZip(sourceDirPath, downloadFileName, response);
    }


    @GetMapping(value = "/chat/gen/code", produces = MediaType.TEXT_EVENT_STREAM_VALUE)
    @RateLimit(limitType = RateLimitType.USER, rate = 5, rateInterval = 60, message = "AI 对话请求过于频繁，请稍后再试")
    public SseEmitter chatToGenCode(@RequestParam Long appId,
                                     @RequestParam String message,
                                     HttpServletRequest request,
                                     HttpServletResponse response) {
        // 设置 SSE 响应头，防止各级代理和容器缓冲
        response.setHeader("Cache-Control", "no-cache");
        response.setHeader("Connection", "keep-alive");
        response.setHeader("X-Accel-Buffering", "no");
        response.setCharacterEncoding("UTF-8");
        // 参数校验
        ThrowUtils.throwIf(appId == null || appId <= 0, ErrorCode.PARAMS_ERROR, "应用ID无效");
        ThrowUtils.throwIf(StrUtil.isBlank(message), ErrorCode.PARAMS_ERROR, "用户消息不能为空");
        // 获取当前登录用户
        User loginUser = userService.getLoginUser(request);
        // 调用服务生成代码（流式）
        Flux<String> contentFlux = appService.chatToGenCode(appId, message, loginUser);
        // 使用 SseEmitter 避免 Spring MVC 响应式适配层缓冲
        SseEmitter emitter = new SseEmitter(600_000L);

        // 自适应流式发送：
        // - 真流式模型（如 flash）：chunk 逐步到达，直接转发
        // - 推理模型（如 pro）：chunk 一次性到达（batch），缓冲后以 20ms 间隔回放
        final long[] chunkCount = {0};
        final long startTime = System.currentTimeMillis();
        final java.util.List<String> chunkBuffer = java.util.Collections.synchronizedList(new java.util.ArrayList<>());
        final long[] lastChunkTime = {-1};

        contentFlux.subscribe(
                chunk -> {
                    try {
                        chunkCount[0]++;
                        lastChunkTime[0] = System.currentTimeMillis();
                        chunkBuffer.add(chunk);
                    } catch (Exception e) {
                        log.error("收集 chunk 异常", e);
                    }
                },
                error -> {
                    log.error("SSE 流异常, totalChunks={}, elapsed={}ms", chunkCount[0], System.currentTimeMillis() - startTime, error);
                    try {
                        emitter.send(SseEmitter.event().name("business-error")
                                .data("{\"error\":true,\"message\":\"生成失败，请重试\"}"));
                        emitter.send(SseEmitter.event().name("done").data(""));
                    } catch (IOException ignored) {}
                    emitter.complete();
                },
                () -> {
                    long totalChunks = chunkBuffer.size();
                    long streamDuration = lastChunkTime[0] - startTime;
                    log.info("SSE 流完成, totalChunks={}, streamDuration={}ms, 开始平滑发送",
                            totalChunks, streamDuration);

                    // 始终以 20ms 间隔逐步回放所有 chunk
                    new Thread(() -> {
                        try {
                            for (int i = 0; i < chunkBuffer.size(); i++) {
                                Map<String, String> wrapper = Map.of("d", chunkBuffer.get(i));
                                emitter.send(SseEmitter.event().data(JSONUtil.toJsonStr(wrapper)));
                                if (i < chunkBuffer.size() - 1) {
                                    Thread.sleep(5);
                                }
                            }
                            emitter.send(SseEmitter.event().name("done").data(""));
                            log.info("SSE 平滑发送完成, totalChunks={}", totalChunks);
                        } catch (Exception e) {
                            log.error("平滑发送异常", e);
                        } finally {
                            emitter.complete();
                        }
                    }, "sse-smooth-send").start();
                }
        );
        return emitter;
    }

